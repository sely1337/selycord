// @ts-check
/*
 * Selycord — Post-build obfuscation
 * Applied to generated .js BEFORE .asar packaging and electron-builder.
 * Automatically disabled in --dev / --watch mode.
 */

import { execSync } from "child_process";
import { statSync } from "fs";
import { join } from "path";

// ─── Target files ───────────────────────────────────────────────────────────
// dist/js/   → buildDesktop (Selycord app itself)
const DESKTOP_JS_FILES = [
    "dist/js/main.js",
    "dist/js/preload.js",
    "dist/js/splashPreload.js",
    "dist/js/updaterPreload.js",
    "dist/js/renderer.js",
];

// dist/desktop/ and dist/Selycord/ → build.mjs (vencord asar)
const ASAR_JS_FILES = [
    "patcher.js",
    "preload.js",
    "renderer.js",
];

// ─── javascript-obfuscator args ─────────────────────────────────────────────
// NOTE: self-defending, rc4 encoding, split-strings and numbers-to-expressions
//       caused freezes when enabling plugins / opening settings.
//       → base64 instead of rc4, reduced threshold, no split-strings.
const OBF_ARGS = [
    "--compact", "true",
    "--self-defending", "false",
    "--simplify", "true",
    "--identifier-names-generator", "hexadecimal",
    "--string-array", "true",
    "--string-array-encoding", "base64",
    "--string-array-threshold", "0.5",
    "--string-array-rotate", "true",
    "--string-array-shuffle", "true",
    "--string-array-index-shift", "true",
    "--string-array-wrappers-count", "1",
    "--string-array-wrappers-type", "variable",
    "--split-strings", "false",
    "--numbers-to-expressions", "false",
    "--unicode-escape-sequence", "false",
].join(" ");

// ─── Helper ─────────────────────────────────────────────────────────────────

function obfuscateFile(filePath) {
    try {
        statSync(filePath);
    } catch {
        console.warn(`[obfuscate] Skipped (not found): ${filePath}`);
        return;
    }

    console.log(`[obfuscate] ${filePath}`);
    try {
        execSync(
            `npx javascript-obfuscator "${filePath}" --output "${filePath}" ${OBF_ARGS}`,
            { stdio: "inherit" }
        );
    } catch (e) {
        console.error(`[obfuscate] FAILED: ${filePath}\n${e.message}`);
        process.exit(1);
    }
}

// ─── Export: for buildDesktop.mts ────────────────────────────────────────────
/**
 * Obfuscates files in dist/js/ (generated by buildDesktop).
 */
export async function obfuscateDistJs() {
    console.log("\n[obfuscate] === Obfuscation dist/js/ ===");
    for (const file of DESKTOP_JS_FILES) {
        obfuscateFile(file);
    }
    console.log("[obfuscate] === Done dist/js/ ===\n");
}

// ─── Export: for build.mjs ───────────────────────────────────────────────────
/**
 * Obfuscates .js files in a dist/ directory before createPackage() into .asar.
 * @param {string} dir  e.g. "dist/desktop" or "dist/Selycord"
 */
export async function obfuscateDir(dir) {
    console.log(`\n[obfuscate] === Obfuscation ${dir}/ ===`);
    for (const filename of ASAR_JS_FILES) {
        obfuscateFile(join(dir, filename));
    }
    console.log(`[obfuscate] === Done ${dir}/ ===\n`);
}
